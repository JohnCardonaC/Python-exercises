
# 1. Write a Python program to sum all the items in a list.

enteros = [1, 2, 3, 4, 5, 7, 10]
sumNumbers = 0
for i in enteros:
    sumNumbers += i
print(sumNumbers)


# 2. Write a Python program to multiply all the items in a list

enterosMult = [5, 2, 5]
multNumbers = 1
for x in enterosMult:
    multNumbers *= x
print(multNumbers)
